	   		<h3>RESPOND</h3>
We Focus on the Greatest Need
While there are almost always multiple communities heavily impacted by a natural disaster, the bulk of the relief efforts are often focused in limited areas— often driven by media coverage. All Hands Volunteers, however, is deeply committed to seeking out the most under-served communities which are often outside of the public eye. As a result, we are often the only organization serving in those communities where damage is as great as—or greater than—more publicized locations.


	   	</p>

	   	   	
	   		<p>
	   			<h3>We Collaborate with Local, National, and International Partners</h3>
All Hands Volunteers is committed to rebuilding the communities that we serve in the smartest way possible, which means that:

We recognize that local organizations understand the needs of their communities better than we do.
We will not compete to help our neighbors. If another organization is better positioned to help, we will partner with them and support their efforts rather than duplicating them.

	   		</p>  	
	  	   		

	   		<p>

	   		<h3>We Take the Long View on the Communities that We Serve</h3>
All Hands Volunteers doesn’t just respond to the initial damage sustained from a natural disaster, we stay to help communities recover and rebuild. While some organizations stay just a few days following a disaster, All Hands Volunteers averages 148 days on a project. As such, we always have an eye toward the long term. Some examples include:

Designing rebuilding plans with local experts that ensure that the materials used are easily accessible and replaceable in case of another disaster.
Working with local officials to create and implement plans that ensure that destroyed villages are rebuilt in safer areas.
Adding much needed facilities like evacuation centers in high risk areas.</p>


	   		<p>

	   		<h3>We Work Hand-in-Hand with Community Members</h3>
A key objective for All Hands Volunteers is to work together with members of the community in order to rebuild confidence and hope in addition to much needed skills and job opportunities. To accomplish this important objective, we offer job training opportunities and certification programs to locals according to the needs of the community.</p>


	   		<p>

	   		<h3>We Prepare to Learn and Be Inspired</h3>
When all of this comes together, there is a truth that is consistently evident – in the process of responding, rebuilding, and renewing the communities that we serve, we are transformed by the resilience, hope, and strength we find in the hearts of the people we meet in the process</p>
